    private void LogInBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LogInBTActionPerformed
        String GivenUserName=UserNameField.getText().trim();
        String GivenPassword=PasswordField.getText().trim();
        //if username field is empty
        if (GivenUserName.isBlank()){
            JOptionPane.showMessageDialog(null, "Please fill the user name field!");    
        }
        
        // if password field is empty
        else if(GivenPassword.isBlank()){       
            JOptionPane.showMessageDialog(null, "Please fill the password field");
        }
        
        // Right user name and password
        else if(isValidEmail(GivenUserName)&&isValidPassword(GivenPassword)){
           new MainMenu().setVisible(true);
        }
        
        //Wrong user name or password were given
        else{
            JOptionPane.showMessageDialog(null, "Wrong user name or password","Message",JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_LogInBTActionPerformed

