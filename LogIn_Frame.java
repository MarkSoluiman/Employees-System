    public boolean isValidEmail(String s){
        fileReader.openEmailFile();
        boolean isValid=false;
        for (String email:fileReader.readEmailFile()){
            if (s.equals(email)){
                isValid=true;
                break;
            }

        }
        fileReader.closeEmailFile();
        return isValid;
    }
    
    public boolean isValidPassword(String s){
        boolean isValid=false;
        fileReader.openPasswordFiles();
        for(String password:fileReader.readPasswordFile()){
            if(password.equals(s)){
                isValid=true;
                break;
                
            }
        }
        fileReader.closePasswordFile();
                
        return isValid;
    }
            


